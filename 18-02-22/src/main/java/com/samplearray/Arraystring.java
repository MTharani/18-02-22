package com.samplearray;

public class Arraystring {

	public static void main(String[] args) {
		String str[]=new String[5];
		str[0]=" -sara";
		str[1]=" -thara";
		str[2]=" -devi";
		str[3]=" -moni";
		str[4]=" -vino";
		for(int i=0;i<str.length;i++)
		{
			System.out.println(+i+str[i]);
		}


	}

}
